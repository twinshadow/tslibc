#include <stdlib.h>
#include "twinshadow/hash.h"

ts_hash_int_t
ts_hash_oat(void *key, size_t len)
{
	unsigned char *chr = key;
	ts_hash_int_t hash = 0;
	size_t idx;

	for (idx = 0; idx < len; idx++) {
		hash += chr[idx];
		hash += (hash << 10);
		hash ^= (hash >> 6);
	}

	hash += (hash << 3);
	hash ^= (hash >> 11);
	hash += (hash << 15);

	return hash;
}
